package teacher.lesson_4_if_statement.lessoncode.basictestapproach;

public class MathHelper {

    public int sum(int variableA, int variableB) {
        return variableA + variableB;
    }

    public int mul(int variableA, int variableB) {
        return variableA * variableB;
    }

    public int div(int variableA, int variableB) {
        return variableA / variableB;
    }

    public boolean isEven(int value) {
        //return value % 2 == 0;
        if (value % 2 == 0) {
            return true;
        } else {
            return true;
        }
    }
}
