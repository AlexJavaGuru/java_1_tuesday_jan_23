package student_vladislav_romanov.lesson_3.level_7;

class Computer {

    private String manufacturer;
    private String model;


    Computer(String manufacturer, String model) {
        this.manufacturer = manufacturer;
        this.model = model;
    }


    String getManufacturer() {
        return manufacturer;
    }

    String getModel() {
        return model;
    }

}
