package student_vladislav_romanov.lesson_6.level_7;

class Move {
    protected int x;
    protected int y;

    public Move() {
    }

    public Move(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}