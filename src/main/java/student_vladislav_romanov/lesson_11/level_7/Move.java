package student_vladislav_romanov.lesson_11.level_7;

public class Move {

    private int x;
    private int y;

    public Move() {
    }

    public Move(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
