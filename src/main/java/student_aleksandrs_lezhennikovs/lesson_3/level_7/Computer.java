package student_aleksandrs_lezhennikovs.lesson_3.level_7;

class Computer {

    String manufacturer;
    String model;

    Computer(String manufacturer, String model) {
        this.manufacturer = manufacturer;
        this.model = model;
    }

    String getManufacturer() {
        return manufacturer;
    }

    String getModel() {
        return model;
    }

}
