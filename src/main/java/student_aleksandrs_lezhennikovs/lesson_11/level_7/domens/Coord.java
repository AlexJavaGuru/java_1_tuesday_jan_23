package student_aleksandrs_lezhennikovs.lesson_11.level_7.domens;

public class Coord {
    private int X;
    private int Y;

    public Coord(int x, int y) {
        X = x;
        Y = y;
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }
}
